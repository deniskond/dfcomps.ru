import { Q3Const } from '../const/q3-const';

export class PlayerState {
  commandTime: number = 0;
  pm_type: number = 0;
  bobCycle: number = 0;
  pm_flags: number = 0;
  pm_time: number = 0;
  origin: number[] = [0, 0, 0];
  velocity: number[] = [0, 0, 0];
  weaponTime: number = 0;
  gravity: number = 0;
  speed: number = 0;
  delta_angles: number[] = [0, 0, 0];
  groundEntityNum: number = 0;
  legsTimer: number = 0;
  legsAnim: number = 0;
  torsoTimer: number = 0;
  torsoAnim: number = 0;
  movementDir: number = 0;
  grapplePoint: number[] = [0, 0, 0];
  eFlags: number = 0;
  eventSequence: number = 0;
  events: number[] = new Array(Q3Const.MAX_PS_EVENTS).fill(0);
  eventParms: number[] = new Array(Q3Const.MAX_PS_EVENTS).fill(0);
  externalEvent: number = 0;
  externalEventParm: number = 0;
  externalEventTime: number = 0;
  clientNum: number = 0;
  weapon: number = 0;
  weaponstate: number = 0;
  viewangles: number[] = [0, 0, 0];
  viewheight: number = 0;
  damageEvent: number = 0;
  damageYaw: number = 0;
  damagePitch: number = 0;
  damageCount: number = 0;
  stats: number[] = new Array(Q3Const.MAX_STATS).fill(0);
  persistant: number[] = new Array(Q3Const.MAX_PERSISTANT).fill(0);
  powerups: number[] = new Array(Q3Const.MAX_POWERUPS).fill(0);
  ammo: number[] = new Array(Q3Const.MAX_WEAPONS).fill(0);
  generic1: number = 0;
  loopSound: number = 0;
  jumppad_ent: number = 0;
  ping: number = 0;
  pmove_framecount: number = 0;
  jumppad_frame: number = 0;
  entityEventSequence: number = 0;

  copy(other: PlayerState): void {
    this.commandTime = other.commandTime;
    this.pm_type = other.pm_type;
    this.bobCycle = other.bobCycle;
    this.pm_flags = other.pm_flags;
    this.pm_time = other.pm_time;
    this.origin = [...other.origin];
    this.velocity = [...other.velocity];
    this.weaponTime = other.weaponTime;
    this.gravity = other.gravity;
    this.speed = other.speed;
    this.delta_angles = [...other.delta_angles];
    this.groundEntityNum = other.groundEntityNum;
    this.legsTimer = other.legsTimer;
    this.legsAnim = other.legsAnim;
    this.torsoTimer = other.torsoTimer;
    this.torsoAnim = other.torsoAnim;
    this.movementDir = other.movementDir;
    this.grapplePoint = [...other.grapplePoint];
    this.eFlags = other.eFlags;
    this.eventSequence = other.eventSequence;
    this.events = [...other.events];
    this.eventParms = [...other.eventParms];
    this.externalEvent = other.externalEvent;
    this.externalEventParm = other.externalEventParm;
    this.externalEventTime = other.externalEventTime;
    this.clientNum = other.clientNum;
    this.weapon = other.weapon;
    this.weaponstate = other.weaponstate;
    this.viewangles = [...other.viewangles];
    this.viewheight = other.viewheight;
    this.damageEvent = other.damageEvent;
    this.damageYaw = other.damageYaw;
    this.damagePitch = other.damagePitch;
    this.damageCount = other.damageCount;
    this.stats = [...other.stats];
    this.persistant = [...other.persistant];
    this.powerups = [...other.powerups];
    this.ammo = [...other.ammo];
    this.generic1 = other.generic1;
    this.loopSound = other.loopSound;
    this.jumppad_ent = other.jumppad_ent;
    this.ping = other.ping;
    this.pmove_framecount = other.pmove_framecount;
    this.jumppad_frame = other.jumppad_frame;
    this.entityEventSequence = other.entityEventSequence;
  }
}
