  private static putSym(sym: number, path: number): void {
    let node = Q3HuffmanMapper.root!;

    while (path > 1) {
      if (path & 0x1) {
        if (node.right === null) {
          node.right = new Q3HuffmanNode();
        }
        node = node.right;
      } else {
        if (node.left === null) {
          node.left = new Q3HuffmanNode();
        }
        node = node.left;
      }
      path >>= 1;
    }

    node.symbol = sym;
  }
